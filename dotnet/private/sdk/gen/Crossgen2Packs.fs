module Crossgen2Packs


open System.IO
open System.Text.Json
open System.Collections.Generic
open System.Text

type Crossgen2Pack = { id: string; version: string }

let private crossgen2PackLabel tfm rid =
    $"//dotnet/private/sdk/crossgen2_packs:{tfm}_{rid}"

let updateCrossgen2Packs crossgen2PacksFile =
    let crossgen2Packs = File.ReadAllText crossgen2PacksFile

    let crossgen2Packs =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, Crossgen2Pack>>>(crossgen2Packs)

    let updatedCrossgen2Packs = Dictionary<string, Dictionary<string, Crossgen2Pack>>()

    for tfmPacks in crossgen2Packs do
        let updatedtfmPacks = Dictionary<string, Crossgen2Pack>()

        for ridPacks in tfmPacks.Value do
            let pack = ridPacks.Value
            let majorVersion = pack.version.Split('.')[0]
            let featureVersion = pack.version.Split('.')[1]

            let latestVersion =
                NugetHelpers.getAllVersions pack.id
                |> List.filter (fun v -> v.ToFullString().StartsWith($"{majorVersion}.{featureVersion}"))
                |> List.max

            let updatedPack =
                { pack with
                    version = latestVersion.ToFullString() }

            updatedtfmPacks.Add(ridPacks.Key, updatedPack)

        updatedCrossgen2Packs.Add(tfmPacks.Key, updatedtfmPacks)

    let updatedCrossgen2PacksJson =
        JsonSerializer.Serialize(updatedCrossgen2Packs, options = JsonSerializerOptions(WriteIndented = true))

    File.WriteAllText(crossgen2PacksFile, updatedCrossgen2PacksJson)
    ()

let writeCrossgen2PackLookupTable crossgen2PacksFile output =
    let crossgen2PacksJson = File.ReadAllText crossgen2PacksFile

    let crossgen2Packs =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, Crossgen2Pack>>>(crossgen2PacksJson)

    let lookupTable = Dictionary<string, Dictionary<string, string>>()

    for tfmPacks in crossgen2Packs do
        lookupTable.Add(tfmPacks.Key, Dictionary<string, string>())

        for ridPacks in tfmPacks.Value do
            let label = crossgen2PackLabel tfmPacks.Key ridPacks.Key
            lookupTable.[tfmPacks.Key].Add(ridPacks.Key, label)

    let lookupTableJson =
        JsonSerializer.Serialize(lookupTable, options = JsonSerializerOptions(WriteIndented = true))

    let sb = new StringBuilder()

    sb.AppendLine("\"GENERATED BY SDK GENERATOR\"") |> ignore
    sb.AppendLine() |> ignore
    sb.Append("crossgen2_pack_lookup_table = ") |> ignore
    sb.Append(lookupTableJson) |> ignore
    sb.Append("\n") |> ignore

    File.WriteAllText(output, sb.ToString())
    ()


let generateCrossgen2PackTargets crossgen2PacksFile output =
    let crossgen2PacksJson = File.ReadAllText crossgen2PacksFile

    let crossgen2Packs =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, Crossgen2Pack>>>(crossgen2PacksJson)

    let sb = new StringBuilder()

    sb.AppendLine("\"GENERATED BY SDK GENERATOR\"") |> ignore
    sb.AppendLine() |> ignore

    sb.AppendLine("load(\"//dotnet/private/sdk/crossgen2_packs:crossgen2_pack.bzl\", \"crossgen2_pack\")")
    |> ignore

    sb.AppendLine() |> ignore
    sb.AppendLine("# buildifier: disable=unnamed-macro") |> ignore
    sb.AppendLine("def crossgen2_packs():") |> ignore
    sb.AppendLine("    \"\"\"crossgen2 packs\"\"\"") |> ignore
    sb.AppendLine() |> ignore

    for tfmPacks in crossgen2Packs do
        for ridPacks in tfmPacks.Value do
            let pack =
                $"\"@dotnet.crossgen2_packs//{ridPacks.Value.id.ToLower()}.v{ridPacks.Value.version}:files\""

            let label = $"{tfmPacks.Key}_{ridPacks.Key}"

            sb.AppendLine($"    crossgen2_pack(name = \"{label}\", pack = {pack})")
            |> ignore

    File.WriteAllText(output, sb.ToString())

    ()

let generateCrossgen2PacksNugetRepo crossgen2PacksFile outputFolder =
    let crossgen2PacksJson = File.ReadAllText crossgen2PacksFile

    let crossgen2Packs =
        JsonSerializer.Deserialize<Dictionary<string, Dictionary<string, Crossgen2Pack>>>(crossgen2PacksJson)

    let repoPackages: NugetRepo.NugetRepoPackage seq =
        crossgen2Packs
        |> Seq.collect (fun sdk -> sdk.Value |> Seq.map (fun tfmPacks -> tfmPacks.Value))
        |> Seq.distinctBy (fun p -> $"{p.id}.{p.version}")
        |> Seq.map (fun pack ->
            let packageInfo =
                NugetHelpers.getPackageInfo pack.id pack.version NugetHelpers.nugetV3Feed

            { name = $"{pack.id.ToLower()}.v{pack.version}"
              id = pack.id
              version = pack.version
              sha512 = packageInfo.sha512sri
              sources = [ NugetHelpers.nugetV3Feed ]
              netrc = None
              dependencies = Dictionary<string, string seq>()
              targeting_pack_overrides = packageInfo.overrides
              framework_list = packageInfo.frameworkList
              tools = Dictionary() })

    NugetRepo.generateBazelFiles "crossgen2_packs" repoPackages outputFolder "dotnet."
    ()
